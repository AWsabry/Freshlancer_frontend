const locales = { en, it, ar };

export function getContractComponentsT(lang) {
  const b = en;
  const o = locales[lang] || {};
  return { ...b, ...o, percentTotal: o.percentTotal ?? b.percentTotal };
}

export function milestoneStatusLabel(t, status) {
  if (!status) return t.status_unknown;
  const k = `status_${String(status).toLowerCase()}`;
  return t[k] || status;
}
