export function getClientWalletT(lang) {
  const base = getStudentWalletT(lang);
  const e = extra[lang] || extra.en;
  const defaultParty =
    lang === 'ar' ? 'طالب' : lang === 'it' ? 'Studente' : 'Student';
  return {
    ...base,
    titleEscrow: e.escrowByProject,
    clientDetails: e.studentDetails,
    defaultClient: defaultParty,
    ...e,
  };
}
